-- Excel GeoFlow
- Только Excel 2013
- Загружается отдельно, как надстройка на Excel
- Найдём его в Конструктор > Map > GeoFlow
- Требуется подключение к интернету, чтобы грузились карты (сервис bing).
- Россию понимает, но не всё может распознать
- Нужны данные, которые имеют географическую составляющую (например субъекты РФ)
- Конечный продукт - презентация
- Каждый слайд это не статичная картинка, это сценарий
- Bing пока на англ, но можно использовать долготу и широту, чтобы не привязываться к названиям
- Проценты, рядом с данными - это сколько %-ов загрузилось, которые не смогли определиться

-- Как работает
	0. Выбираем данные в Excel и запускаем GeoFlow
	1. Подключаем свой ресурс (например Excel)
	2. Выбираем поле
	3. Указываем в Bing, что означает это поле (например state)
	4. Выбираем столбик, который будет отображает высоту столбика (3 вида диаграммы: столбики, пузыри,тепловая карта)
	5. Когда слайд устраивает > Add Scene
	6. Потом можно старый слайд сделать динамичным (всеможная анимация)
	7. При добавлении слайдов, можно включить сразу несколько слайдов одновременно, чтобы сравнить данные
- Можно классифицировать по годам, если добавить года в Category(Лучше работает с малым количеством категорий)
- Столбики

