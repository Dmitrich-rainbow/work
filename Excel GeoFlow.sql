-- Анимация по оси времени
- Обязательно нужен столбец с типом даты
- Там, где выбираем какие поля используем, есть поле Time > Появляется полузнок (Time Line) и Time Decorator
- Если хотим чтобы показывались данные в реальном времени, то в Time Settings разные параметры 
- Чтобы настроить скорость анимации, надо после сохранения ролика зайти в его настройки

